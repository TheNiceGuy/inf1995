#ifndef INCLUDE_MESSAGE_H
#define INCLUDE_MESSAGE_H

/** Cette macro définie le numéro du message pour le nom du robot. */
#define MSG_NOM_ROBOT 0xF0

/** Cette macro définie le numéro du message pour le numéro de l'équipe. */
#define MSG_NUMERO_EQUIPE 0xF1

/** Cette macro définie le numéro du message pour la section. */
#define MSG_NUMERO_SECTION 0xF2

/** Cette macro définie le numéro du message pour la session. */
#define MSG_SESSION 0xF3

/** Cette macro définie le numéro du message pour la couleur de la base. */
#define MSG_COULEUR_BASE 0xF4

/** Cette macro définie le numéro du message pour l'état du bouton Interrupt. */
#define MSG_ETAT_INTERRUPT 0xF5

/** Cette macro définie le numéro du message pour la distance du capteur gauche. */
#define MSG_DISTANCE_GAUCHE 0xF6

/** Cette macro définie le numéro du message pour la distance du capteur droit. */
#define MSG_DISTANCE_DROITE 0xF7

/** Cette macro définie le numéro du message pour la vitesse de la roue gauche. */
#define MSG_VITESSE_GAUCHE 0xF8

/** Cette macro définie le numéro du message pour la vitesse de la roue droite. */
#define MSG_VITESSE_DROITE 0xF9

/** Cette macro définie le numéro du message pour le contrôle de la DEL libre. */
#define MSG_COULEUR_DEL 0xFA

/** Cette macro définie le numéro du message pour la requête d'information. */
#define MSG_REQUETE_INFO 0xFB

/** Cette macro définie le numéro du message pour la DEL libre soit éteinte. */
#define MSG_DEL_ETEINT 0

/** Cette macro définie le numéro du message pour la DEL libre soit verte. */
#define MSG_DEL_VERTE 1

/** Cette macro définie le numéro du message pour la DEL libre soit rouge. */
#define MSG_DEL_ROUGE 2

#endif
